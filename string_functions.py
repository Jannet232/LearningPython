#STEP 1: User to input string
input_string = input("Enter a string value: ")
print(f'You entered : {input_string} \n__________________________________________________')


#STEP 2: Convert string to lower case
new_string = input_string.lower()
print(f'Applying method lower() on string \n {new_string}')

#STEP 3: Convert string to upper case
new_string = input_string.upper()
print(f'Applying method upper() on string \n {new_string}')

#STEP 4: Convert string to title
new_string = input_string.title()
print(f'Applying method title() on string \n {new_string}')

#STEP 5: Swap case of string
new_string = input_string.swapcase()
print(f'Applying method swapcase() on string \n {new_string}')\


